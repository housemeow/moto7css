        <p>為了精進MOTO7教練團騎乘技術，提升騎術訓練活動以及安全駕駛內容，MOTO7主編楊斌與安駕講師小天特別安排到日本的Suzuka 安全駕駛教育中心，來學習最新的騎乘技巧與觀念，並且將這些知識帶回台灣，讓大家也能夠學到最完整的安全駕駛技巧。</p>
        
        <img src="images/contentpic1.png">
        <p><b>Suzuka 交通安全中心介紹</b><br/>
        HONDA自1964年開始為回饋交通安全，開始研究交通安全，並且在Suzuka 設立第一間交通安全中心，直至今日在日本境內共有7間交通安全中心，另外在37個國家也都有安駕中心或活動進行，旨在進行社會回饋，提升交通安全，新竹安駕中心的前身也曾是HONDA 交通安全中心，在HONDA 與三陽分家後，才獨立出來。</p>
        
        <img src="images/contentpic2.png">
        <p>
        Suzuka 交通安全中心位於鈴鹿國際賽車場旁，擁有佔地70,000平方米的場地，整個訓練設施包含一般環場道路、道路實境模擬、低摩擦係數路面、剎車訓練場、雨天訓練場、中低速綜合訓練場、模擬市區道路以及主建築，其設施極為完善，搭配HONDA 交通安全研究團隊四十多年來的研究結晶，成為交通安全研究的指標性學校。</p>
